# ASNI Color Codes
class Colors:
    HEADER = '\033[95m'         # Purple
    BLUE = '\033[94m'           # Blue
    CYAN = '\033[96m'           # Cyan
    GREEN = '\033[92m'          # Green
    WARNING = '\033[93m'        # Yellow
    FAIL = '\033[91m'           # Red
    ORANGE = '\033[38;5;208m'   # Orange
    WHITE = '\033[97m'          # White
    ENDC = '\033[0m'            # Reset
    BOLD = '\033[1m'            # Bold
    UNDERLINE = '\033[4m'       # Underline